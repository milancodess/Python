# Variables can be stored in alphabets, numbers, and underscores.
# Variables cannot start with a number.
# Variables can only be start with an alphabet or underscore.
# No whitespaces are allowed in variable names.
# Variables can be assigned using the assignment operator '='.

a = 1

aaa = 1

milan = 1

_milan = 1

